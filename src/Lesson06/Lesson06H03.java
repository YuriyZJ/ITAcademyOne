//3. Необходимо вывести на экран таблицу умножения на 3:

public class Lesson06H03 {
    public static void main(String[] args){
        int a;

        for(int i = 1; i <= 10; i++){
            a = 3 * i;
            System.out.println( "3 * " + i + " = " + a);
        }
    }
}
